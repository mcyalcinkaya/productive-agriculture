session_start();
if (isset($_POST['login'])) {
    $username = $_POST['username'];
    $password = $_POST['password'];

    // Database connection
    $conn = new mysqli('localhost', 'root', '', 'agriculture_project');  // Change database name to agriculture_project
    if ($conn->connect_error) {
        die("Database connection error: " . $conn->connect_error);
    }

    // User authentication
    $stmt = $conn->prepare("SELECT * FROM users WHERE username = ? AND password = ?");
    $stmt->bind_param("ss", $username, $password);
    $stmt->execute();
    $result = $stmt->get_result();
    if ($result->num_rows === 1) {
        $user = $result->fetch_assoc();
        $_SESSION['user_id'] = $user['id'];  // Store user ID in session
        $_SESSION['assigned_area'] = $user['assigned_area'];  // Store assigned area instead of role

        // Redirect to dashboard
        header('Location: dashboard.php');  // Assuming dashboard.php handles the dashboard
        exit;
    } else {
        $error = "Invalid username or password!";
    }
    $stmt->close();
    $conn->close();
}
